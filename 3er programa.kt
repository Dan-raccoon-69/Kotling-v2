
fun main(){
    print("Ingresa el valor de a: ")
    val a = readln().toInt()

    print("Ingresa el valor de b: ")
    val b = readln().toInt()

    println("$a + $b = ${a+b}")
    println("$a - $b = ${a-b}")
    println("$a * $b = ${a*b}")
    println("$a / $b = ${a/b}")
    println("$a % $b = ${a%b}")

}